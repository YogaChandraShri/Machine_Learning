import pandas as pd
from sklearn.linear_model import LogisticRegression

"""**LOADING THE DATA**"""

dia=pd.read_excel('/content/diabetes.xlsx')

"""**VIEWING THE DATA**"""

dia.head()

"""CLEANING THE DATA
CHECKING FOR NULL VALUES **bold text**
"""

dia.isnull().sum()

"""**SPLITING THE DEPENDENT AND INDEPENDENT VARIABLES**"""

indep=dia[['mass','insu','age','pres']]
dep=dia[['class']]

"""**CREATING INSTANCE FOR LOGISTIC REGRESSION**"""

Log=LogisticRegression()

Log.fit(indep,dep)

"""**PREDICTING THE RESULT**"""

w=int(input("Enter the weight:"))
ins=int(input("Enter the insulin level:"))
age=int(input("Enter the age:"))
pres=int(input("Enter the blood pressure:"))
print("Your test result is",Log.predict([[w,ins,age,pres]]))

"""**DESCRIBES THE RESULT'S CORRECTNESS (0.6 -> 60% CORRECT)**"""

Log.score(indep,dep)

from sklearn.metrics import accuracy_score
pval=Log.predict(indep)
accuracy_score(dep,pval)
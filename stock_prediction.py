import pandas as pd
from sklearn.linear_model import LinearRegression

"""**LOADING THE DATA**"""

stock=pd.read_csv('/content/Quote-Equity-JUBLFOOD-EQ-10-06-2024-to-10-06-2025.csv')

"""**VIEWING THE DATA**"""

stock.head(10)

"""**CLEANING THE DATA**
CHECKING THE DATA WHETHER IT CONTAINS NULL VALUES
"""

stock.isnull().sum()

"""**REMOVING THE NULL VALUES**"""

stock.dropna(inplace=True)

"""**ONCE AGAIN CHECKING FOR NULL VALUES TO VERIFY**"""

stock.isnull().sum()

"""**SPLITING THE INDEPENDENT AND DEPENDENT VARIABLES**"""

indep=stock[['VOLUME ','HIGH ','LOW ','close ','OPEN ']]
dep=stock['Next_day_open']

"""**CREATING INSTANCES FOR LINEAR REGRESSION MODULE**"""

Lr=LinearRegression()

Lr.fit(indep,dep)

"""**NEXT DAY OPENING PRICE OF THE JUBILANT FOODWORKS LIMITED STOCK IS PREDICTED.**"""

Lr.predict([[457279,700.15,692.05,695.85,694.70]])